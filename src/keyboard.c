char readScanCode() {
	return inb(0x60);
}

char c = 0;

char waitScanCode() {
    do {
        if(inb(0x60)!=c) {
            c=inb(0x60);
            if (c > 0)
				break;
        }
    } while(1);

    //~ outb(0x20, 0x20);
    return c;
}

